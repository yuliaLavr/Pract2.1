public class Room {
    int num;
    int fl;
    String type;

    public Room(int num, int fl, String type) {
        this.num = num;
        this.fl = fl;
        this.type = type;
    }

    public int getNum() {
        return num;
    }

    public int getFl() {
        return fl;
    }

    public String getType() {
        return type;
    }
}
